// Arithmetic Operations
const PI = 3.14159;

// Operations
const sum = (x, y) => x + y;
const substraction = (x, y) => x - y;
const multiplication = (x, y) => x * y;
const division = (x, y) => x / y;

const circumference = (r) => PI * r * r;

// Exports
module.exports = { sum, substraction, multiplication, division, circumference };